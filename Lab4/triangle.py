#The area function accepts the triangle's base and height 
#as an argument and returns the area of the triangle.
def area(base, height):
    return (base * height) / 2

#The perimeter function accepts the length of the triangle's sides 
#as arguments and returns the perimeter of the triangle.
def perimeter(side1, side2, side3):
    return (side1 + side2 + side3)

